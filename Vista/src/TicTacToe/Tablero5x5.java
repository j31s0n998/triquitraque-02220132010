public void press(int lugar) {
    if (labels[lugar - 1].getText().isEmpty()) { // Verificar si la casilla está vacía
        labels[lugar - 1].setText(turn); // Asignar el turno

        // Aplicar el color del texto según el turno
        if (turn.equals("x")) {
            labels[lugar - 1].setForeground(colorX); // Usar color para "X"
        } else {
            labels[lugar - 1].setForeground(colorO); // Usar color para "O"
        }

        giveUpTheTurn(); // Cambiar el turno
        verifyWinner(); // Verificar si hay ganador

        // Verificar si todas las casillas están llenas
        if (isBoardFull() && !isWinner()) {
            resetGame(); // Reiniciar el juego si está lleno y no hay ganador
            JOptionPane.showMessageDialog(this, "Empate, el juego se reiniciará");
        }
    }
}

private boolean isBoardFull() {
    for (JLabel label : labels) {
        if (label.getText().isEmpty()) {
            return false; // Si alguna casilla está vacía, el tablero no está lleno
        }
    }
    return true; // Si todas las casillas están llenas
}

private boolean isWinner() {
    for (int[] combo : victory) {
        boolean xWins = true;
        boolean oWins = true;

        // Verificar si la combinación actual es una victoria para "X" o "O"
        for (int index : combo) {
            if (!labels[index].getText().equals("x")) {
                xWins = false; // No todas las celdas son "x"
            }
            if (!labels[index].getText().equals("o")) {
                oWins = false; // No todas las celdas son "o"
            }
        }

        if (xWins) {
            return true; // "X" gana
        } else if (oWins) {
            return true; // "O" gana
        }
    }

    return false; // No hay ganador
}
public void giveUpTheTurn (){
       if(turn.equals("x")){  
          turn = "o";
 
        }   else{
                turn ="x";
            }
 
 }    
    private int puntuacionX = 0; // Victorias de jugador X
private int puntuacionO = 0; // Victorias de jugador O

public void verifyWinner() {
    for (int[] combo : victory) {
        boolean xWins = true;
        boolean oWins = true;

        // Verificar si todas las celdas en la combinación tienen "x" o "o"
        for (int index : combo) {
            if (!labels[index].getText().equals("x")) {
                xWins = false; // No todas las celdas son "x"
            }
            if (!labels[index].getText().equals("o")) {
                oWins = false; // No todas las celdas son "o"
            }
        }

        if (xWins) { // Si "x" gana
            puntuacionX++; // Incrementar la puntuación de X
            labelVictoryX.setText(" ganadas: " + puntuacionX); // Actualizar el marcador
            showWinnerMessage("X"); // Mostrar mensaje de ganador
            resetGame(); // Reiniciar el juego
            break; // Salir del bucle
        }

        if (oWins) { // Si "o" gana
            puntuacionO++; // Incrementar la puntuación de O
            labelVictoryO.setText("ganadas: " + puntuacionO); // Actualizar el marcador
            showWinnerMessage("O"); // Mostrar mensaje de ganador
            resetGame(); // Reiniciar el juego
            break; // Salir del bucle
        }
    }
}

